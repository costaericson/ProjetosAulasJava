package Arrays;

import java.util.Iterator;

public class Foreach {
	public static void main(String[] args) {
		double [] a = {5.4, 8.9, 10.0};
		
		for (int i = 0; i < a.length; i++) {
			System.out.println(a[i] + " ");
			
		}
		
		for (double nota: a) {
			System.out.println(nota + " ");
		}
		
			
		}
		
		
	}

